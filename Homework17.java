class Homework17{
	public static void main(String[]args){
		System.out.println("int to double");
		int a = 25;
		double b = a;
		
		System.out.println(b);
		
		// character to ascii
		System.out.println("char to ASCII");
		char c = 'B';
		int d = c;
		System.out.println(d);
		
		//explicit casting
		System.out.println("explicit casting");
		double e = 45.89;
		int f = (int) e;
		System.out.println(f);
	}
}